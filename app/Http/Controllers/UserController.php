    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $query = User::query();

        if (request('buscar')) {
            $query->where('name', 'LIKE', '%' . request('buscar') . '%');
        }

        return Inertia::render('User/List', ['usuarios' => $query->paginate(10)->withQueryString()]);

    }

